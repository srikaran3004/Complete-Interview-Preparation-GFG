#include <bits/stdc++.h>
using namespace std;


// psuedo code:- 

class MyCalc
{
public:
    static int add(int a, int b)
    {

        return a + b;
    }

    static int sub(int x, int y)
    {

        return x - y;
    };

    static int mul(int x, int y)
    {

        return x * y;
    };
};